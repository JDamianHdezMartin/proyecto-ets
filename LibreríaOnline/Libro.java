public class Libro{
    String isbn;
    String titulo;
    int fechaPublicacion;
    String autor;
    String editorial;

    public Libro(String isbn, String titulo, int fechaPublicacion, String autor, String editorial) {
        this.isbn = isbn;
        this.titulo = titulo;
        this.fechaPublicacion = fechaPublicacion;
        this.autor = autor;
        this.editorial = editorial;
    }

}